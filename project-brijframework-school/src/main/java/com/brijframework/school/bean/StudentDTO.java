package com.brijframework.school.bean;

public class StudentDTO {
	
	private Long rollNo;
	private String srno;
	private Long lfNo;
	
	public Long getRollNo() {
		return rollNo;
	}
	
	public void setRollNo(Long rollNo) {
		this.rollNo = rollNo;
	}
	
	public String getSrno() {
		return srno;
	}
	
	public void setSrno(String srno) {
		this.srno = srno;
	}
	
	public Long getLfNo() {
		return lfNo;
	}
	
	public void setLfNo(Long lfNo) {
		this.lfNo = lfNo;
	}
	
}
